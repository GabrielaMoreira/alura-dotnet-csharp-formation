            Produto p3 = new Produto();
            p3.Nome = "O Monge e o Executivo";
            p3.Categoria = "Livros";
            p3.Preco = 19.89;


            using (var contexto = new LojaContext())
            {
                contexto.Produtos.Add(p3);
                contexto.SaveChanges();
            }
        }

        private static void GravarUsandoAdoNet()
        {
